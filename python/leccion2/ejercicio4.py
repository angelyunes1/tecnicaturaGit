# Ejercicio 4 // Etapas de la vida
etapa = int(input("Ingrese su edad :"))
if 0 <= etapa <= 10:
    print("la infancia es increible y bella")
elif 11 <= etapa <= 19:
    print("Tienes muchos cambios, mucho que estudiar")
elif 20 <= etapa <= 29:
   print("Amor y comienza el trabajo")
elif 30 <= etapa <= 39:
    print("Seguis trabajando")
elif 40 <= etapa <= 59:
    print("Falta poco para jubilarte")
elif 60 <= etapa <= 120:
    print("Disfruta de tu ultima etapa de vida")
else:
    print("Ingresaste un numero equivocado")

